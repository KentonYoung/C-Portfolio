#include <iostream>
using namespace std;

// A C++ Program to output a greeting.

int main()
{
	cout << "Hello World!" << endl;

	int firstNumber;
	firstNumber = 12;
	cout << "firstNumber = " << firstNumber << endl;

	float firstFloat = 0.12345;
	cout << "firstFloat = " << firstFloat << endl;

	double firstDouble; firstDouble = 0.123456789;
	cout << "firstDouble = " << firstDouble << endl;

	bool isMyFirstBool = false;
	cout << "isMyFirstBool = " << isMyFirstBool << endl;

	char firstChar; firstChar='A';
	cout << "firstChar = " << firstChar << endl;

	return 0 ;
}
